package com.class17_2;

public class Nested {
	public static void main(String[] args) {
		for(int i=1; i<5; i++) {
			for(int y=1; y<6; y++) {
				System.out.print(y);
			}
			System.out.println();
		}
		for(int b=1; b<=6; b++) {
			for(int c=1; c<=6; c++) {
				System.out.print(b);
			}
			System.out.println();
		}
	
	for(int d=1; d<=5; d++) {
		for(int f=5; f>=1; f--) {
			System.out.print(f);
		}
		System.out.println();
	}

}
}